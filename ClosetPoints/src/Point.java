
public class Point {
	private double x;
	private double y;
	private int num;
	public double getX() {
		return x;
	}
	public double getY() {
		return y;
	}
	public int getNum() {
		return num;
	}
	public Point(double x, double y,int num) {
		super();
		this.x = x;
		this.y = y;
		this.num = num;
	}
}
